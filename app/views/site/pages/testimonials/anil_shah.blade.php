							<p>यो देश आज फेरि एक पटक नेपालको नयाँ संविधान लेख्न  र लागू गर्ने महान् कार्यका लागि संविधान सभाको सदस्य चुन्ने महत्वपूर्ण कामको अगाडि उभिएको छ । </p>
							<p>लोकतान्त्रिक मुलकका नागरिकका हैसियतमा हामीले आफूलाई प्रतिनिधित्व गर्ने मानिस छान्ने अधिकारको प्रयोग मात्र होइन कि, संविधान लेख्ने महत्वपूर्ण कार्यका लागि सही व्यक्ति छान्ने जिम्मेवारी पनि पूरा गर्नुपर्ने छ ।</p> 
							<p>यसका लागि अनेक राजनीतिक पार्टी र धेरै उम्मेद्वारहरू हाम्रो मतका लागि प्रतिष्पर्धा गरिरहेका अवस्थामा जनताको मतको जिमेवारीबोध गर्ने प्रतिनिधि छनौट गर्नका लागि उनीहरुबारे सूचना कहाँ पाउने भन्ने महत्वपूर्ण प्रश्न छ । कसलाई मत दिने भन्ने निर्णय हामी कसरी गर्न सक्छौँ ? </p>
							<p>यही सूचनाको अभाव पूर्ति गर्ने प्राथमिक उद्देश्यका साथ हाम्रो नेता डट कम (www.hamroneta.com) सुरु गरिएको छ । आआफ्ना विचारका आधारमा कसलाई मत दिने र कसलाई नदिने भनेर सुझाउने धेरै व्यक्ति हाम्रा अगाडि छन् । तर हामीमध्ये धेरैलाई हाम्रा निर्वाचन क्षेत्रका उम्मेद्वारबारे जानकारी पाउने ठाउँको अभाव महशुस भइरहेको छ, जसले गर्दा हामी निर्णय गर्न नसक्ने अवस्थामा छौँ । हाम्रो नेता डट कमले गर्न खोजेको खास काम नै त्यो अवस्था अन्त्य गरी सही निर्णय गर्न सक्षम बनाउनु हो । </p>
							<p>हाम्रो नेता डट कम कुनै पार्टी वा उम्मेद्वारको समर्थन वा विरोध गर्दैन, बरु तीबारे सूचना प्राप्त गर्न  र आफ्ना धारणा राख्न सकिने एक मञ्च उपलब्ध गराउँछ  । यो मञ्चमा हामीलाई संविधान सभामा प्रतिनिधित्व गर्ने भनेर खडा भएका उम्मेद्वारको जीवनी मात्र हुनेछैन कि खुला लोकतान्त्रिक तरिकामा मुद्दा र व्यक्तिबारे धारणा राख्ने र छलफल गर्ने अवसर पनि उपलब्ध हुनेछ । यससँगै हामी जनता आफूले आफूलाई प्रतिनिधित्व गर्न चाहने व्यक्तिलाई ‘अनौपचारिक’ मत दिन पाउनेछौँ र जनता के सोच्दै छन् भन्ने थाहा पाउन सक्नेछौँ । 
								अन्ध समर्थन र भिडको पछाडि लाग्दा आउने परिणाम हामी सबैले देखिसकेका छौँ । गत चुनावभन्दा धेरै टाढा नपुगिसकेको तर धेरै दुःख पाइसकेको अवस्थामा यस पालि हामीले केही फरक गर्नुपर्छ । केही भइसकेपछि टिप्पणी गर्नु धेरै ढिलो हुनेछ, बरु निर्णय गर्नु र मत दिनु अगाडि नै सुसूचित हुनु आवश्यक छ । हाम्रो नेता डट कम खासमा त्यसकै लागि एक तयार पारिएको मञ्च हो । </p>
							<p>लोकतान्त्रिक शासन प्रणाली त्यस्तो होइन कि जहाँ जनता किनारमा बसेर सत्तामा रहेका नेताबारे गुनासो  गर्छन् । यो त्यस्तो शासन प्रणाली हो जहाँ जनताले जनतामध्येकै सर्वोत्कृष्ट व्यक्तिलाई सरकारको नेतृत्वका लागि छान्न सक्रिय भूमिका खेल्छन् , ताकि तिनले देशलाई आजभन्दा भोलि राम्रो बनाऊन् । कुनै पनि लोकतान्त्रिक देशको सबभन्दा महत्वपूर्ण र सर्वशक्तिमान स्तम्भ नै हामी नागरिक हौँ । यसर्थ हामीसँग हामीलाई सरकारमा प्रतिनिधित्व गर्ने सही व्यक्ति छान्ने प्रयास गर्ने एक महान् जिम्मेवारी छ । यस कार्यका लागि हामीलाई सक्षम बनाउन हाम्रा अगाडि उभिएका उम्मेद्वारबारेका सूचना महत्वपूर्ण हुन्छन् । हाम्रो नेता डट कम हामीलाई त्यस्ता सूचना उपलब्ध गराउने एक पोर्टल हो ।   
								हामी पुनः दोहोर्याउँछौँ कि हाम्रो नेता डट कमले कसलाई मत दिने बताउँदैन, तर हामी सबैलाई आफूले संविधानका पानामा के देख्न चाहान्छौँ भन्ने कुरालाई सबभन्दा राम्रोसँग प्रतिनिधित्व गर्न सक्ने उम्मेद्वारबारे निर्णय गर्नका लागि सूचना उपलब्ध गराउँछ । हामी, ‘हामी जनता’लाई आफूलाई आवश्यक सूचना लिन, प्रतिक्रिया दिन र छलफलमा भाग लिन हाम्रो नेता डट कमको प्रयोग गर्न प्रोत्साहन गर्छौँ ता कि मत दिन मत बाकसतर्फ लम्कँदा हामीलाई हामी कसलाई मत दिँदै छौँ मात्र होइन, हामी उनीहरूलाई किन मत दिँदै छौँ भन्ने पनि थाहा हुनेछ ।</p>
							<span id="writer">- अनिल शाह (नेपाली नागरिक) <br> Translated by Devashish Neupane</span>


