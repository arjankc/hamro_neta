						<div id="english">
							
							
							As a nation we once again stand at the crucial juncture of electing our Constituent Assembly to commence the herculean task of formulating and promulgating the Constitution of Nepal.<br><br>

        As a democracy we have to accept not only our right to elect the people who will represent us, but also our responsibility to elect the ‘right’ individuals to complete this crucial task.<br><br>

        To this end with the multitude of political parties and the many, many candidates vying for our vote, the critical question is how do we get the information we need to make an informed choice. How can we make up our mind on who to cast our vote for?<br><br>

        It is with the primary objective to fill this information void that hamroneta.com has been set up. We have enough people telling us who to vote for and who not to vote for in their respective opinions, but many of us were at a loss for a place we could go to, to get the information we needed on the candidates that are standing from our constituency so that we could make up our minds for ourselves. This is what hamroneta.com endeavors to do.<br><br>

        Hamroneta.com does not support or oppose any political party or candidate, but provides a platform in which information on the candidates can be obtained and views can be shared. It is a venue where not only can we get biographies on the candidates standing across the nation to represent us in the Constituent Assembly, but also a place where we can engage in discussions and share our views on issues and individuals, in an open democratic manner. Along with this we the people, can also give our ‘informal’ vote on who you would prefer to be your representative in the Constituent Assembly, so that we can all see how we the people are thinking.<br><br>

        We have all seen the results of following blind faith and going with the crowd. Having run very hard and not gone very far the last time round, we have to do things differently this time. It is too late to comment after the fact, we need to have the information we need prior to making our decision and casting our vote, and hamroneta.com is a platform that seeks to do just that.<br><br>

        A democratic system of government is not one in which the citizens sit on the side lines and complain about the leaders in power, it is a system of polity where the people take an active role in choosing the very best from amongst us to take the reins of government in their hands and lead us to a better tomorrow. The most important and powerful pillar of any democracy is ‘Us’ the citizens. But along with this comes a great responsibility upon the people, the responsibility to take the time and make the effort to choose the right individuals to represent us in government. And one of the crucial factors that enables us to do this is having the information we need on the candidates standing before us and hamroneta.com is a portal that will provide that information to us all.<br><br>

        I reiterate once again hamroneta.com is not going to tell you who to vote for, but endeavor to give you enough information so that each of us can individually decide which candidate we feel would best represent what we want to see in the pages of our nation’s Constitution. I encourage us all ‘Hamijanta’ to use hamroneta.com to get the information we need, give your comments and engage in the discussions therein, so that at the end of the day when we walk up to the ballot box we know not only who we are going to vote for, but more importantly why we are voting for them!  <br><br>

        <span id="writer">-Anil Shah, Nepali Nagarik</span>
							
							
							
